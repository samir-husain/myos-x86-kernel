static isr_t handlers[256] = {0};

#define PIC1 0x20
#define PIC2 0xA0
#define PIC1_COMMAND 0x20
#define PIC1_DATA 0x21
#define PIC2_COMMAND 0xA0
#define PIC2_DATA 0xA1

void pic_set_masks(uint8_t master_mask, uint8_t slave_mask)
{
    outb(PIC1_DATA, master_mask);
    outb(PIC2_DATA, slave_mask);
}

void pic_remap(uint8_t offset1, uint8_t offset2)
{
    uint8_t a1 = inb(PIC1_DATA);
    uint8_t a2 = inb(PIC2_DATA);

    outb(PIC1_COMMAND, 0x11);
    io_wait();
    outb(PIC2_COMMAND, 0x11);
    io_wait();

    outb(PIC1_DATA, offset1);
    io_wait();
    outb(PIC2_DATA, offset2);
    io_wait();

    outb(PIC1_DATA, 0x04);
    io_wait();
    outb(PIC2_DATA, 0x02);
    io_wait();

    outb(PIC1_DATA, 0x01);
    io_wait();
    outb(PIC2_DATA, 0x01);
    io_wait();

    outb(PIC1_DATA, a1);
    outb(PIC2_DATA, a2);
}

static void pic_ack(uint8_t int_no)
{
    if (int_no >= 40)
        outb(PIC2_COMMAND, 0x20);
    outb(PIC1_COMMAND, 0x20);
}

void register_interrupt_handler(uint8_t n, isr_t handler)
{
    handlers[n] = handler;
}

uint32_t isr_handler(regs_t *r)
{
    uint8_t n = (uint8_t)r->int_no;

    if (handlers[n])
    {
        handlers[n](r);
    }
    else
    {
        // minimal default log
        serial_puts("INT ");
        serial_put_hex8(n);
        serial_puts(" ERR ");
        serial_put_hex32(r->err_code);
        serial_putc('\n');
    }

    // ACK PIC if IRQ
    if (n >= 32 && n <= 47)
    {
        pic_ack(n);
    }

    // Preemptive switch only on PIT IRQ0 (int 32 = 0x20)
    regs_t *out = r;
    if (n == 0x20)
    {
        out = task_schedule(r);
    }

    return (uint32_t)out;
}

